# ---------------------------------------------------------------------------
# Config
# ---------------------------------------------------------------------------

GUILD_CLI_DIR = os.environ.get(
    "GUILD_CLI_DIR",
    str(Path(__file__).resolve().parent.parent),
)
GATE_BIN = str(Path(GUILD_CLI_DIR) / "bin" / "gate.mjs")
GUILD_BIN = str(Path(GUILD_CLI_DIR) / "bin" / "guild.mjs")

# content_root: where guild.config.yaml lives
_DEFAULT_CONTENT_ROOT = os.environ.get("GATE_CONTENT_ROOT", "")
_PROJECTS_DIR = os.environ.get(
    "GATE_PROJECTS_DIR",
    str(Path(_DEFAULT_CONTENT_ROOT) / "projects") if _DEFAULT_CONTENT_ROOT else "",
)

# Mutable — switched by gate_switch_project()
_current_content_root = _DEFAULT_CONTENT_ROOT
_current_project: str | None = None

mcp = FastMCP(
    "gate-mcp",
    instructions="guild-cli gate commands as MCP tools. Supports project switching via gate_switch_project().",
)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _cwd() -> str | None:
    """Return the current content root for subprocess cwd."""
    return _current_content_root or None


async def _run_gate(*args: str, stdin_text: str | None = None) -> str:
    """Run gate CLI and return stdout. Raises on non-zero exit."""
    cmd = ["node", GATE_BIN, *args]
    proc = await asyncio.create_subprocess_exec(
        *cmd,
        stdin=asyncio.subprocess.PIPE if stdin_text else None,
        stdout=asyncio.subprocess.PIPE,
        stderr=asyncio.subprocess.PIPE,
        cwd=_cwd(),
    )
    stdout, stderr = await proc.communicate(
        input=stdin_text.encode() if stdin_text else None
    )
    out = stdout.decode().strip()
    err = stderr.decode().strip()
    if proc.returncode != 0:
        raise RuntimeError(f"gate exited {proc.returncode}: {err or out}")
    # Append stderr warnings if any
    if err:
        out = f"{out}\n[stderr] {err}"
    return out


async def _run_guild(*args: str) -> str:
    """Run guild CLI and return stdout."""
    cmd = ["node", GUILD_BIN, *args]
    proc = await asyncio.create_subprocess_exec(
        *cmd,
        stdout=asyncio.subprocess.PIPE,
        stderr=asyncio.subprocess.PIPE,
        cwd=_cwd(),
    )
    stdout, stderr = await proc.communicate()
    out = stdout.decode().strip()
    err = stderr.decode().strip()
    if proc.returncode != 0:
        raise RuntimeError(f"guild exited {proc.returncode}: {err or out}")
    if err:
        out = f"{out}\n[stderr] {err}"
    return out


def _flag(name: str, value: str | None) -> list[str]:
    """Build --name value if value is truthy."""
    return [f"--{name}", value] if value else []


