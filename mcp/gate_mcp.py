_PROJECT_CONFIG_TEMPLATE = """\
# Auto-generated by gate-mcp for project: {project}
content_root: .
host_names: [eris, nao]
paths:
  members: members
  requests: requests
  issues: issues
  inbox: inbox
"""


def _project_dir(project: str) -> Path:
    """Return the directory for a named project. Validates path stays under PROJECTS_DIR."""
    if not project or "/" in project or "\\" in project or project in (".", ".."):
        raise ValueError(f"Invalid project name: {project!r}")
    resolved = (Path(_PROJECTS_DIR) / project).resolve()
    base = Path(_PROJECTS_DIR).resolve()
    if not str(resolved).startswith(str(base) + os.sep) and resolved != base:
        raise ValueError(f"Project path escapes base: {project!r}")
    return resolved


@mcp.tool()
async def gate_switch_project(project: str | None = None) -> str:
    """Switch gate context to a project. Pass None or empty string to return to THS global.

    If the project directory doesn't exist yet, use gate_init_project() first.
    """
    global _current_content_root, _current_project

    if not project:
        _current_content_root = _DEFAULT_CONTENT_ROOT
        _current_project = None
        return f"Switched to global content root: {_DEFAULT_CONTENT_ROOT}"

    proj_dir = _project_dir(project)
    if not proj_dir.exists():
        return f"Project '{project}' not found at {proj_dir}. Use gate_init_project('{project}') to create it."

    config_file = proj_dir / "guild.config.yaml"
    if not config_file.exists():
        return f"Project directory exists but guild.config.yaml is missing at {config_file}."

    _current_content_root = str(proj_dir)
    _current_project = project
    return f"Switched to project '{project}' at {proj_dir}"


@mcp.tool()
async def gate_init_project(project: str) -> str:
    """Initialize a new project with guild.config.yaml, shared members, and empty request/issue dirs."""
    proj_dir = _project_dir(project)

    if proj_dir.exists() and (proj_dir / "guild.config.yaml").exists():
        return f"Project '{project}' already exists at {proj_dir}"

    # Create directories
    for subdir in ["requests/pending", "requests/completed", "issues", "inbox"]:
        (proj_dir / subdir).mkdir(parents=True, exist_ok=True)

    # Symlink members from global content root (shared across projects)
    members_link = proj_dir / "members"
    if not members_link.exists():
        global_members = Path(_DEFAULT_CONTENT_ROOT) / "members"
        if global_members.exists():
            members_link.symlink_to(global_members.resolve())

    # Write config
    config_file = proj_dir / "guild.config.yaml"
    config_file.write_text(_PROJECT_CONFIG_TEMPLATE.format(project=project))

    return f"Initialized project '{project}' at {proj_dir}\n  members: symlinked from global\n  requests/issues/inbox: project-local"


@mcp.tool()
async def gate_list_projects() -> str:
    """List available gate projects."""
    global _current_project
    projects_dir = Path(_PROJECTS_DIR)
    if not projects_dir.exists():
        return "No projects directory found."

    projects = sorted(
        d.name for d in projects_dir.iterdir()
        if d.is_dir() and (d / "guild.config.yaml").exists()
    )

    if not projects:
        return "No projects initialized yet. Use gate_init_project() to create one."

    lines = []
    for p in projects:
        marker = " (active)" if p == _current_project else ""
        lines.append(f"  - {p}{marker}")

    current = _current_project or "(global)"
    return f"Current: {current}\nProjects:\n" + "\n".join(lines)


